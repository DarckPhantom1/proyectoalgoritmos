    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String password = new String(txtPassword.getPassword());  
        String passwordBase64 = Base64.getEncoder().encodeToString(password.getBytes());
       
      
     
        conexionSQL con = new conexionSQL();
        Connection conexion = con.conexion();
        try {
          
            String query = "SELECT * FROM usuarios WHERE usuario = ? AND pass = ?";
            PreparedStatement pst = conexion.prepareStatement(query);
            pst.setString(1, usuario);
            pst.setString(2, password);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                 nombreUsuario = usuario;
                 userRol = rs.getString("rol");           
               
                JOptionPane.showMessageDialog(null, "Login exitoso"+nombreUsuario);
                formMenuPrincipal menu = new formMenuPrincipal();
                menu.setVisible(true);
                
                if(usuario.equals(password)){
                     JOptionPane.showMessageDialog(null, "Es necesario cambio de password inmediatamente ");   
                    cambioPassword objeto= new cambioPassword();
                    objeto.setVisible(true); 
                    
                }
                
               
      
                this.dispose();  
            } else {
               
                JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos");
            }

       
            rs.close();
            pst.close();
            conexion.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al conectar con la base de datos: " + e.getMessage());
        }
    }

